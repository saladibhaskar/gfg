class Solution {
  public:
    vector<int> Series(int n) {
        // Code here
        vector<int>v={0,1};
        for(int i=2;i<=n;i++)
        {
            int a=(v[i-1]+v[i-2])%1000000007;
            v.push_back(a);
        }
        return v;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution obj;

        vector<int> ans = obj.Series(n);
        for (auto x : ans) cout << x << " ";
        cout << "\n";
    }
    return 0;
}
